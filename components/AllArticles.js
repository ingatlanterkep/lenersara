// Segédfüggvény a dátum ISO formátumra konvertálásához (mindenhol ugyanazt használjuk)
export const getIsoDate = (hungarianDate) => {
  const dateParts = hungarianDate.match(/(\d{4})\.\s*(\w+)\s*(\d{1,2})\./);
  if (!dateParts) return '2026-01-01T00:00:00+01:00';

  const year = dateParts[1];
  const monthStr = dateParts[2].toLowerCase();
  const day = dateParts[3].padStart(2, '0');

  const monthMap = {
    január: '01', február: '02', március: '03', április: '04',
    május: '05', június: '06', július: '07', augusztus: '08',
    szeptember: '09', október: '10', november: '11', december: '12'
  };

  const month = monthMap[monthStr] || '01';
  return `${year}-${month}-${day}T00:00:00+01:00`;
};

